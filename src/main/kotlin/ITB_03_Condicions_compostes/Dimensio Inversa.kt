package ITB_03_Condicions_compostes/*
Els físics que surten als comics de Marvel diuen que existeixen dimensions inverses, a on hi ha personatges que fan de tots els nostres defectes virtuds. Nosaltres invertirem alguna cosa moralment menys complexa.

Entrada:
Cada cas és d'una sola línia que contindrá un número.

Sortida:
Per cada cas es dirà el número invertit de forma fraccionària. Per exemple, 2
serà 1/2

Per simplicitat no hi haurà casos amb decimals periòdics.
 */

import java.util.*
fun main()
{
    val scanner = Scanner(System.`in`)
    val num = scanner.nextFloat() // Llegeix el número amb el float

    println(1/num) // Imprimeix el num invertit de forma fraccionària





}